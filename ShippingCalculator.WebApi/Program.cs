using ShippingCalculator.WebApi.Configurations;
using ShippingCalculator.WebApi.Services;

namespace ShippingCalculator.WebApi
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddConfigurations();

            builder.Services.AddSingleton<ICdecTokenService, CdecTokenService>();
            builder.Services.AddHttpClient<ICdecApiService, CdecApiService>();

            builder.Services.AddControllers();
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();

            app.MapControllers();

            app.Run();
        }
    }
}
